console.log("DO-WHILE:");
var numero = 1;
do {
    console.log("Numero vale: ", numero)
    numero = numero + 1;
} while (numero <= 100);

console.log("WHILE:");
numero = 1;
while (numero <= 100) {
    console.log("Numero vale: ", numero)
    numero = numero + 1;
}

console.log("FOR:");
for (let i = 1; i <= 100; i++) {
    console.log("Numero vale: ", i);
}